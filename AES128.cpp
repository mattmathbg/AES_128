// Constantes de ronde (Rcon)
static const uint8_t rcon[11] = {
    0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36
};

//prend un bloc de 16 octets et le chiffre grace l'algorithme AES-128 
void AES128::encryptBlock(std::array<uint8_t, 16>& state) const {
    //XOR de l'état avec la clé de ronde 0 (la clé maître)
    addRoundKey(state, 0);

    //9 rondes principales
    for (int round = 1; round <= 9; round++) {
        subBytes(state);
        shiftRows(state);
        mixColumns(state);
        addRoundKey(state, round);
    }

    //Ronde finale (sans mixColumns pour que le dechiffrement soit symetrique avec le chiffrement) 
    subBytes(state);
    shiftRows(state);
    addRoundKey(state, 10);
}


//génère les clés de ronde à partir de la clé maître
void AES128::keyExpansion(const std::vector<uint8_t>& key) {
    //copie d'abord les 16 octets de la clé maître
    for (int i = 0; i < 16; i++) {
        roundKeys[i] = key[i];
    }

    int bytesGenerated = 16;
    int rconIndex = 1;
    uint8_t temp[4];

    //genère les 10 clés de ronde suivantes 
    while (bytesGenerated < 176) {
        temp[0] = roundKeys[bytesGenerated - 4];
        temp[1] = roundKeys[bytesGenerated - 3];
        temp[2] = roundKeys[bytesGenerated - 2];
        temp[3] = roundKeys[bytesGenerated - 1];

        if (bytesGenerated % 16 == 0) {
            //rotword : rotation circulaire à gauche
            uint8_t t = temp[0];
            temp[0] = temp[1];
            temp[1] = temp[2];
            temp[2] = temp[3];
            temp[3] = t;

            //sbox : substitution octet par octet
            temp[0] = sbox[temp[0]];
            temp[1] = sbox[temp[1]];
            temp[2] = sbox[temp[2]];
            temp[3] = sbox[temp[3]];

            //rcon : XOR avec la constante de ronde
            temp[0] ^= rcon[rconIndex];
            rconIndex++;
        }

        //Pour trouver par exemple roundKeys[16], on fait XOR de temp avec roundKeys[0] (16 octets avant)
        roundKeys[bytesGenerated]     = roundKeys[bytesGenerated - 16] ^ temp[0];
        roundKeys[bytesGenerated + 1] = roundKeys[bytesGenerated - 15] ^ temp[1];
        roundKeys[bytesGenerated + 2] = roundKeys[bytesGenerated - 14] ^ temp[2];
        roundKeys[bytesGenerated + 3] = roundKeys[bytesGenerated - 13] ^ temp[3];

        //on avance de 4 octets pour la prochaine itération (prochain mot de 4 octets à générer)
        bytesGenerated += 4;
    }
}

//constructeur qui prend la clé maître et génère les clés de ronde
AES128::AES128(const std::vector<uint8_t>& key) {
    keyExpansion(key);
}


//prend un bloc de 16 octets chiffré et le déchiffre grâce à l'algorithme AES-128
void AES128::decryptBlock(std::array<uint8_t, 16>& state) const {
    addRoundKey(state, 10);
    invShiftRows(state);
    invSubBytes(state);

    for (int round = 9; round >= 1; round--) {
        addRoundKey(state, round);
        invMixColumns(state);
        invShiftRows(state);
        invSubBytes(state);
    }

    addRoundKey(state, 0);
}

//Substitution octet par octet via la S-Box
void AES128::subBytes(std::array<uint8_t, 16>& state) const {
    for (int i = 0; i < 16; i++) {
        state[i] = sbox[state[i]];
    }
}

//XOR de l'état avec la clé de ronde courante
void AES128::addRoundKey(std::array<uint8_t, 16>& state, int round) const {
    int offset = round * 16;
    for (int i = 0; i < 16; i++) {
        state[i] ^= roundKeys[offset + i];
    }
}

//Décalage cyclique des lignes de la matrice état
void AES128::shiftRows(std::array<uint8_t, 16>& state) const {
    uint8_t temp;

    // Ligne 0 : Inchangée (indices 0, 4, 8, 12)

    // Ligne 1 : Décalage de 1 vers la gauche (indices 1, 5, 9, 13)
    temp = state[1];
    state[1] = state[5];
    state[5] = state[9];
    state[9] = state[13];
    state[13] = temp;

    // Ligne 2 : Décalage de 2 vers la gauche (indices 2, 6, 10, 14)
    temp = state[2];
    state[2] = state[10];
    state[10] = temp;
    temp = state[6];
    state[6] = state[14];
    state[14] = temp;

    // Ligne 3 : Décalage de 3 vers la gauche = 1 vers la droite (indices 3, 7, 11, 15)
    temp = state[15];
    state[15] = state[11];
    state[11] = state[7];
    state[7] = state[3];
    state[3] = temp;
}

//Mélange les colonnes 
void AES128::mixColumns(std::array<uint8_t, 16>& state) const {
    for (int i = 0; i < 4; i++) {
        int c = i * 4;

        uint8_t s0 = state[c];
        uint8_t s1 = state[c + 1];
        uint8_t s2 = state[c + 2];
        uint8_t s3 = state[c + 3];

        state[c]     = multiply(s0, 0x02) ^ multiply(s1, 0x03) ^ s2 ^ s3;
        state[c + 1] = s0 ^ multiply(s1, 0x02) ^ multiply(s2, 0x03) ^ s3;
        state[c + 2] = s0 ^ s1 ^ multiply(s2, 0x02) ^ multiply(s3, 0x03);
        state[c + 3] = multiply(s0, 0x03) ^ s1 ^ s2 ^ multiply(s3, 0x02);
    }
}

// Transformations inverses
void AES128::invSubBytes(std::array<uint8_t, 16>& state) const {
    for (int i = 0; i < 16; i++) {
        state[i] = inv_sbox[state[i]];
    }
}

//Décalage cyclique inverse des lignes de la matrice état
void AES128::invShiftRows(std::array<uint8_t, 16>& state) const {
    uint8_t temp;

    // Ligne 1 : Décalage de 1 vers la DROITE (inverse du décalage gauche de shiftRows)
    // Ordre : 13 -> 9 -> 5 -> 1 -> 13
    temp = state[13];
    state[13] = state[9];
    state[9] = state[5];
    state[5] = state[1];
    state[1] = temp;

    // Ligne 2 : Décalage de 2 (identique dans les deux sens, on échange par paires)
    temp = state[2];
    state[2] = state[10];
    state[10] = temp;
    temp = state[6];
    state[6] = state[14];
    state[14] = temp;

    // Ligne 3 : Décalage de 3 vers la DROITE = 1 vers la GAUCHE
    // Ordre : 3 -> 7 -> 11 -> 15 -> 3
    temp = state[3];
    state[3] = state[7];
    state[7] = state[11];
    state[11] = state[15];
    state[15] = temp;
}


//Mélange inverse des colonnes 
void AES128::invMixColumns(std::array<uint8_t, 16>& state) const {
    for (int i = 0; i < 4; i++) {
        int c = i * 4;
        uint8_t s0 = state[c];
        uint8_t s1 = state[c + 1];
        uint8_t s2 = state[c + 2];
        uint8_t s3 = state[c + 3];

        state[c]     = multiply(s0, 0x0e) ^ multiply(s1, 0x0b) ^ multiply(s2, 0x0d) ^ multiply(s3, 0x09);
        state[c + 1] = multiply(s0, 0x09) ^ multiply(s1, 0x0e) ^ multiply(s2, 0x0b) ^ multiply(s3, 0x0d);
        state[c + 2] = multiply(s0, 0x0d) ^ multiply(s1, 0x09) ^ multiply(s2, 0x0e) ^ multiply(s3, 0x0b);
        state[c + 3] = multiply(s0, 0x0b) ^ multiply(s1, 0x0d) ^ multiply(s2, 0x09) ^ multiply(s3, 0x0e);
    }
}